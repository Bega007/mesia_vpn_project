class ImageService {
  static String image(String image){
    return 'lib/assets/images/$image.png';
  }
  static String svgImages(String image){
    return 'lib/assets/images/$image.svg';
  }

  static String pngIcon(String icon){
    return 'lib/assets/icons/$icon.png';
  }

  static String svgIcon(String icon) {
    return 'lib/assets/icons/$icon.svg';
  }
}